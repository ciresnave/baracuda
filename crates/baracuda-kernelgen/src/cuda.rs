//! CUDA C++ lowering — the one backend-specific module (v1).
//!
//! Everything CUDA-shaped (the `float4` type, `__global__`, the
//! `blockIdx`/`blockDim` launch indexing) lives here. The math itself is lowered
//! by the language-neutral [`crate::backend::lower_expr`]; this module only
//! supplies the leaf accessors and the kernel boilerplate.

use crate::backend::{lower_expr, Backend, GeneratedKernel};
use crate::plan::{KernelPlan, Schedule};
use baracuda_kernels_types::ElementKind;

/// The CUDA C++ backend. Lowers a [`KernelPlan`] to `.cu` source.
#[derive(Copy, Clone, Debug, Default)]
pub struct Cuda;

impl Backend for Cuda {
    fn name(&self) -> &str {
        "cuda"
    }

    fn lower(&self, plan: &KernelPlan<'_>) -> GeneratedKernel {
        assert_eq!(
            plan.dtype,
            ElementKind::F32,
            "cuda backend v1 lowers f32 only"
        );
        match plan.schedule {
            Schedule::Vectorized { width: 4 } => emit_vec4(plan),
            Schedule::Scalar => emit_scalar(plan),
            other => panic!("cuda backend v1: unsupported schedule {other:?}"),
        }
    }
}

fn header(plan: &KernelPlan<'_>, name: &str) -> String {
    format!(
        "// Generated by baracuda-kernelgen — do not edit.\n\
         // op: {} | cell: {}\n\
         #include <cstdint>\n#include <cuda_runtime.h>\n\n",
        plan.op_name, plan.cell,
    ) + &format!("extern \"C\" __global__ void {name}(\n")
}

fn emit_vec4(plan: &KernelPlan<'_>) -> GeneratedKernel {
    let name = format!("baracuda_gen_{}_f32_co_v4", plan.op_name);
    let n = plan.n_inputs;
    let mut s = header(plan, &name);
    for i in 0..n {
        s.push_str(&format!("    const float4* __restrict__ in{i},\n"));
    }
    s.push_str("    float4* __restrict__ out,\n    int64_t n4)\n{\n");
    s.push_str("    int64_t i = (int64_t)blockIdx.x * blockDim.x + threadIdx.x;\n");
    s.push_str("    int64_t step = (int64_t)gridDim.x * blockDim.x;\n");
    s.push_str("    for (; i < n4; i += step) {\n");
    for i in 0..n {
        s.push_str(&format!("        float4 v{i} = in{i}[i];\n"));
    }
    s.push_str("        float4 vo;\n");
    for lane in ["x", "y", "z", "w"] {
        let acc = |idx: u8| format!("v{idx}.{lane}");
        s.push_str(&format!("        vo.{lane} = {};\n", lower_expr(plan.body, &acc)));
    }
    s.push_str("        out[i] = vo;\n    }\n}\n");
    GeneratedKernel { name, source: s }
}

fn emit_scalar(plan: &KernelPlan<'_>) -> GeneratedKernel {
    let name = format!("baracuda_gen_{}_f32_scalar", plan.op_name);
    let n = plan.n_inputs;
    let mut s = header(plan, &name);
    for i in 0..n {
        s.push_str(&format!("    const float* __restrict__ in{i},\n"));
    }
    s.push_str("    float* __restrict__ out,\n    int64_t n)\n{\n");
    s.push_str("    int64_t i = (int64_t)blockIdx.x * blockDim.x + threadIdx.x;\n");
    s.push_str("    int64_t step = (int64_t)gridDim.x * blockDim.x;\n");
    let acc = |idx: u8| format!("in{idx}[i]");
    s.push_str(&format!(
        "    for (; i < n; i += step) out[i] = {};\n}}\n",
        lower_expr(plan.body, &acc)
    ));
    GeneratedKernel { name, source: s }
}

#[cfg(test)]
mod tests {
    use crate::ir::{input, OpDef};
    use crate::{generate, Cuda};
    use baracuda_kernels_types::{structure_key, ArchSku, ElementKind, OpCategory, OperandDesc};

    fn add_op() -> OpDef {
        OpDef::elementwise("add", 2, &[ElementKind::F32], input(0) + input(1))
    }

    #[test]
    fn generates_vectorized_add() {
        let a = OperandDesc::new(1, &[1 << 20], &[1], ElementKind::F32, 256);
        let key = structure_key(OpCategory::BinaryElementwise, &[a, a, a], ArchSku::Sm89);
        let k = generate(&add_op(), &key, &Cuda);
        assert_eq!(k.name, "baracuda_gen_add_f32_co_v4");
        assert!(k.source.contains("extern \"C\" __global__"));
        assert!(k.source.contains("float4 v0 = in0[i];"));
        assert!(k.source.contains("vo.x = (v0.x + v1.x);"));
        assert!(k.source.contains("vo.w = (v0.w + v1.w);"));
        assert!(k.source.contains("out[i] = vo;"));
    }

    #[test]
    fn fma_lowers_nested_expression() {
        let fma = OpDef::elementwise(
            "fma",
            3,
            &[ElementKind::F32],
            input(0) * input(1) + input(2),
        );
        let a = OperandDesc::new(1, &[1 << 20], &[1], ElementKind::F32, 256);
        let key = structure_key(OpCategory::TernaryElementwise, &[a, a, a, a], ArchSku::Sm89);
        let k = generate(&fma, &key, &Cuda);
        assert!(k.source.contains("vo.y = ((v0.y * v1.y) + v2.y);"));
    }

    #[test]
    fn scalar_fallback_when_not_vectorizable() {
        let strided = OperandDesc::new(2, &[1024, 1024], &[1, 1024], ElementKind::F32, 256);
        let key =
            structure_key(OpCategory::BinaryElementwise, &[strided, strided, strided], ArchSku::Sm89);
        let k = generate(&add_op(), &key, &Cuda);
        assert_eq!(k.name, "baracuda_gen_add_f32_scalar");
        assert!(k.source.contains("out[i] = (in0[i] + in1[i]);"));
    }
}
