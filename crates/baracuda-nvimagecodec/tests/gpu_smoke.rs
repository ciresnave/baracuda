/// A minimal valid 1×1 opaque-red PNG (8-bit RGB, single IDAT).
#[rustfmt::skip]
const MIN_PNG: &[u8] = &[
    0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // signature
    0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52, // IHDR len + tag
    0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, // 1x1
    0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53, // bit depth 8, color type 2 (RGB)
    0xDE,
    0x00, 0x00, 0x00, 0x0C, 0x49, 0x44, 0x41, 0x54, // IDAT len + tag
    0x08, 0xD7, 0x63, 0xF8, 0xCF, 0xC0, 0x00, 0x00, // zlib: filter 0 + RGB(255,0,0)
    0x03, 0x01, 0x01, 0x00,
    0x18, 0xDD, 0x8D, 0xB4,                         // IDAT CRC
    0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, // IEND
    0xAE, 0x42, 0x60, 0x82,
];

fn decode_one(bytes: &[u8], label: &str) {
    baracuda_driver::init().unwrap();
    let device = Device::get(0).unwrap();
    let ctx = Context::new(&device).unwrap();

    let instance = Instance::new().expect("nvimgcodecInstanceCreate");
    let decoder = Decoder::new(&instance).expect("nvimgcodecDecoderCreate");

    let stream = CodeStream::from_host_mem(&instance, bytes).expect("code stream");
    let info = stream.image_info().expect("image_info");
    eprintln!(
        "{label}: codec={:?} {}x{} fmt={:?}",
        info.codec_name, info.width, info.height, info.sample_format
    );
    assert_eq!(info.width, 1);
    assert_eq!(info.height, 1);

    let mut buf: DeviceBuffer<u8> =
        DeviceBuffer::zeros(&ctx, (info.width * info.height * 3) as usize).unwrap();
    let image = Image::new_interleaved_rgb8(&instance, &mut buf, info.width, info.height, None)
        .expect("image create");

    let future = decoder
        .decode(&stream, &image, &DecodeParams::default())
        .expect("decode");
    future.wait().expect("future wait");
    assert!(future.all_succeeded().expect("status"), "{label} decode failed");

    // `image` borrows `buf` mutably for its whole scope (compile-time
    // use-after-free guard); drop it before reading the decoded pixels back.
    drop(image);

    let mut host = [0u8; 3];
    buf.copy_to_host(&mut host).unwrap();
    eprintln!("{label}: decoded pixel {host:?}");
}

#[test]
#[ignore = "requires an NVIDIA GPU + installed libnvimgcodec"]
fn decode_tiny_jpeg() {
    decode_one(MIN_JPEG, "jpeg");
}

#[test]
#[ignore = "requires an NVIDIA GPU + installed libnvimgcodec"]
fn decode_tiny_png() {
    decode_one(MIN_PNG, "png");
}

#[test]
#[ignore = "requires an NVIDIA GPU + installed libnvimgcodec"]
fn report_properties() {
    let instance = Instance::new().expect("instance");
    let props = instance.properties().expect("properties");
    eprintln!(
        "nvimgcodec version {:#x}, cudart {:#x}",
        props.version, props.cudart_version
    );
}
